// Product of Numbers

#include <iostream>
using namespace std;

int main()
{
  int num1;
  int num2;
  int product;

  cout << "Enter the No. 1" << endl;
  cin >> num1;
  cout << "Enter the No. 2" << endl;
  cin >> num2;

  product = num1 * num2;

  cout << "Product = " << product << endl;

  return 0;
}
